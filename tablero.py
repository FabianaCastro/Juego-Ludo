def imprimir_tablero(jugador_pos, cpu_pos):
    for i in range(ALTO):
        for j in range(ANCHO):
            if (i, j) == jugador_pos:
                print("*", end="\t")
            elif (i, j) == cpu_pos:
                print("x", end="\t")
            else:
                print(mapa[i][j], end="\t")
        print()

